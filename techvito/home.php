<script>
const wrapper = document.querySelector('.icon-wrapper');
const icons = Array.from(document.querySelectorAll('.icon-item'));

// Clone the icons to create an infinite loop effect
icons.forEach(icon => {
  const clone = icon.cloneNode(true);
  wrapper.appendChild(clone);
});

// Ensuring smooth infinite scroll by duplicating icons
const totalIcons = icons.length;
let currentPosition = 0;

// Smooth looping scroll logic using requestAnimationFrame for smoother animations
function scrollCarousel() {
  currentPosition -= 2; // Adjust this value to control scroll speed

  if (currentPosition <= -(totalIcons * 25)) { // Loop back after all icons are scrolled (100% = 4 icons, so multiply by 25 for each icon)
    currentPosition = 0; // Reset position smoothly
  }

  wrapper.style.transform = `translateX(${currentPosition}%)`;
  requestAnimationFrame(scrollCarousel);
}

scrollCarousel();
</script>

